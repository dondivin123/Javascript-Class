// COMPARISON OPERATORS
// Greater / Less than (< >)
console.log(1>5)
let com = 8 > 3
console.log(com)

// Greater / Less or equals (<= >=)
console.log(4<=2+2)

// Equals (==)
console.log(2==1)
console.log("2"== 2)

// Not equal (!=)
console.log(2 != 2)

// strict equality
console.log("2"===2)

// string comparison
let name = "John"
let s = "joh"
console.log(name > s) // unicode order

